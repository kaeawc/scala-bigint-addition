package example

import org.specs2.mutable._

class BigIntAdditionSpec extends Specification {

  "BigIntAddition" should {

    "add single digit operands" in {

      BigIntAddition.add("1","2") mustEqual "3"

    }

    "largish numbers" in {

      BigIntAddition.add("1050","2234") mustEqual "3284"

    }

    "do carries properly" in {

      BigIntAddition.add("1959","2234") mustEqual "4193"

    }
    
    "add varying length numbers" in {

      BigIntAddition.add("19590","2234") mustEqual "21824"

    }
    
    "add really big numbers" in {

      BigIntAddition.add(
        "111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111",
        "1"
      ) mustEqual "111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111112"

    }
  }
}
